package com.henallux.dondesang.model;

public class Statistique {
    private Integer nbDons;
    private Integer nbUtilisateursInscrit;
    private Integer nbCollecteTot;
    private Integer nbDonsTot;

    public Integer getNbDons() {
        return nbDons;
    }

    public void setNbDons(Integer nbDons) {
        this.nbDons = nbDons;
    }

    public Integer getNbUtilisateursInscrit() {
        return nbUtilisateursInscrit;
    }

    public void setNbUtilisateursInscrit(Integer nbUtilisateursInscrit) {
        this.nbUtilisateursInscrit = nbUtilisateursInscrit;
    }

    public Integer getNbCollecteTot() {
        return nbCollecteTot;
    }

    public void setNbCollecteTot(Integer nbCollecteTot) {
        this.nbCollecteTot = nbCollecteTot;
    }

    public Integer getNbDonsTot() {
        return nbDonsTot;
    }

    public void setNbDonsTot(Integer nbDonsTot) {
        this.nbDonsTot = nbDonsTot;
    }
}
